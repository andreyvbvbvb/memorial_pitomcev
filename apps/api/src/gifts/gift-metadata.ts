const getCodeParts = (code: string) => {
  const match = code.toLowerCase().match(/^([a-z]+)(?:_(\d+))?$/);
  const type = match?.[1];
  if (!type) {
    return null;
  }
  return {
    type,
    number: match[2] ? Number(match[2]) : null
  };
};

export const getGiftMetadata = (code: string, fallbackName?: string | null): GiftMetadata => {
  const parts = getCodeParts(code);
  if (!parts) {
    const name = fallbackName?.trim() || "Подарок памяти";
    return {
      name,
      description: "Небольшой знак внимания и заботы рядом с мемориалом."
    };
  }
  const group = metadataGroups[parts.type];
  if (!group) {
    const name = fallbackName?.trim() || "Подарок памяти";
    return {
      name,
      description: "Небольшой знак внимания и заботы рядом с мемориалом."
    };
  }
  if (!parts.number || Number.isNaN(parts.number)) {
    return {
      name: fallbackName?.trim() || group.fallbackName,
      description: group.fallbackDescription
    };
  }
  const index = (parts.number - 1) % group.names.length;
  return {
    name: group.names[index] ?? group.fallbackName,
    description: group.descriptions[index] ?? group.fallbackDescription
  };
};
